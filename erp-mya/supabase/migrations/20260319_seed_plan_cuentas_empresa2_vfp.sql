  select count(*) into v_base    from public.plan_cuentas_base  b join tmp_vfp v on v.codigo=b.codigo;
  select count(*) into v_empresa from public.plan_cuentas_empresa where empresa_id = 2;
  raise notice '──────────────────────────────────────────';
  raise notice 'Cuentas en plan_cuentas_base (VFP):   %', v_base;
  raise notice 'Cuentas en plan_cuentas_empresa (2):  %', v_empresa;
  raise notice 'Importación completada. ✓';
  raise notice '──────────────────────────────────────────';
end $$;

