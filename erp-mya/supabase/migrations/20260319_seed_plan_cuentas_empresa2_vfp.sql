-- ─── 2. Upsert en plan_cuentas_base ──────────────────────────────────────────
-- Nota: ON CONFLICT actualiza solo el nombre si cambió.
-- Las copias existentes en plan_cuentas_empresa de otras empresas no se tocan.
insert into public.plan_cuentas_base (
  codigo,
  nombre,
  nivel,
  tipo,
  naturaleza,
  acepta_movimiento,
  padre_codigo,
  activo
)
select
  v.codigo,
  v.nombre,
  -- nivel derivado del formato del código
  case
    when v.codigo ~ '^\d{4}-\d{2}-\d{3}-\d{3}$' then 5
    when v.codigo ~ '^\d{4}-\d{2}-\d{3}$'        then 4
    when v.codigo ~ '^\d{4}-\d{2}$'               then 3
    when v.codigo ~ '^\d{4}$'                     then 2
    else 1
  end as nivel,
  v.tipo,
  v.naturaleza,
  -- acepta movimientos solo en nivel 5 (cuentas hoja)
  (v.codigo ~ '^\d{4}-\d{2}-\d{3}-\d{3}$') as acepta_movimiento,
  -- padre calculado del código
  case
    when v.codigo ~ '^\d{4}-\d{2}-\d{3}-\d{3}$' then regexp_replace(v.codigo, '-\d{3}$', '')
    when v.codigo ~ '^\d{4}-\d{2}-\d{3}$'        then regexp_replace(v.codigo, '-\d{3}$', '')
    when v.codigo ~ '^\d{4}-\d{2}$'               then left(v.codigo, 4)
    when v.codigo ~ '^\d{4}$'                     then left(v.codigo, 2)
    else null
  end as padre_codigo,
  true as activo
from tmp_vfp v
on conflict (codigo) do update
  set nombre    = excluded.nombre,
      tipo      = excluded.tipo,
      naturaleza= excluded.naturaleza,
      activo    = true;

-- ─── 3. Copiar a plan_cuentas_empresa para empresa_id = 2 ────────────────────
insert into public.plan_cuentas_empresa (
  empresa_id,
  cuenta_base_id,
  codigo,
  nombre,
  activo
)
select
  2                  as empresa_id,
  b.id               as cuenta_base_id,
  b.codigo,
  b.nombre,
  true               as activo
from public.plan_cuentas_base b
join tmp_vfp v on v.codigo = b.codigo
where not exists (
  select 1
  from public.plan_cuentas_empresa e
  where e.empresa_id = 2
    and e.cuenta_base_id = b.id
)
order by b.nivel, b.codigo;

